void main() {
  int num = 1000;
  switch (num) {
    case 10:
      print('Number is 10');
      break;
    case 20:
      print('Number is 20');
      break;
    case 30:
      print('Number is 30');
      break;
    default:
      print('Number is not 10,20,30');
      print('Number is $num');
  }
}
